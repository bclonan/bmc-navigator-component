// Function to get financial literacy content for a field
export function getFinancialLiteracyContent(fieldName, fieldType = 'text') {
  // Normalize field name
  const normalizedFieldName = fieldName.toLowerCase().replace(/[_\s]/g, '-');
  
  // Find matching content
  const contentKey = fieldToContentMapping[normalizedFieldName];
  
  if (contentKey && financialLiteracyContent[contentKey]) {
    return financialLiteracyContent[contentKey];
  }
  
  // Fallback content based on field type
  const fallbackContent = {
    title: "Financial Guidance",
    content: "This field is important for your loan application. Accurate information helps lenders make the best decision for your financial situation.",
    financialInsight: "Being honest and accurate with your financial information helps ensure you get loan terms that fit your budget and financial goals.",
    icon: "fas fa-info-circle"
  };
  
  return fallbackContent;
}

// Function to get content by key
export function getContentByKey(key) {
  return financialLiteracyContent[key] || null;
}

// Function to get all available content keys
export function getAllContentKeys() {
  return Object.keys(financialLiteracyContent);
}

export default financialLiteracyContent;